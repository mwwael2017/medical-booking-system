const mongoose = require('mongoose');
// In a real app, use bcrypt to hash passwords

const adminSchema = new mongoose.Schema({
    username: {
        type: String,
        required: true,
        unique: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
    role: {
        type: String,
        default: 'admin',
    }
}, {
    timestamps: true,
});

module.exports = mongoose.model('Admin', adminSchema);
